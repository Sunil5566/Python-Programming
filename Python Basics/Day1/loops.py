#For loop
fruits = "apple", "banana", "cherry", "grapes"
for fruit in fruits:
    print(fruit)

#While loop

i = 1
while i<=5:
    print(i)
    i +=1

# Break continue and pass
for i in range(1,10):
    if i == 6:
     break
    print(i)

# Continue    
for i in range(1,10):
    if i == 6:
     continue
    print(i)

#pass   
for i in range(1,10):
    if i == 6:
     pass
    print(i)    

